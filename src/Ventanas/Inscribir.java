    ///---------COMBO BOX MATERIAS--------
    private void materiasCB(){
        jCBMaterias.removeAllItems();
        for (Materia mat : Colegio.getMaterias().values()) {
            jCBMaterias.addItem(mat.getNombre());
        }
    }

    ///---------COMBO BOX ALUMNOS--------
    private void alumnosCB(){
        jCBAlumnos.removeAllItems();
        for (Alumno alum : Colegio.getAlumnos().values()) {
            jCBAlumnos.addItem(alum.getApellido()+" "+alum.getNombre()+" ("+alum.getLegajo()+")");
        }
    }
    
